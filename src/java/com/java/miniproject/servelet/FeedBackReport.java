    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        PrintWriter out = response.getWriter();
        String dateOfReport = request.getParameter("message");
        //out.print(dateOfReport);
//        ChatBean chatBaan = null;
//        chatBaan.setDateOfReport(dateOfReport);
        ArrayList<ChatBean> chatReport = null; 
        String responseText  = "";
        chatReport = new ChatDAO().chatReport();
        for(int i=0;i<chatReport.size();i++){
            if(chatReport.get(i).getDate().equals(dateOfReport)){
                responseText+=chatReport.get(i).getUserName()+"|";
                responseText+=chatReport.get(i).getMessage()+"|";
                responseText+=chatReport.get(i).getDate()+"|";
                responseText+=chatReport.get(i).getTime()+"|";  
            }
        }
        out.print(responseText);
    }

